# int() 함수와 float() 함수 조합하기

input_a = float(input("첫 번쨰 숫자> "))
input_b = float(input("두 번쨰 숫자> "))
print()
print("덧셈 결과 : ", input_a + input_b)
print("뺄셈 결과 : ", input_a - input_b)
print("곱셈 결과 : ", input_a * input_b)
print("나눗셈 결과 : ", input_a / input_b)

